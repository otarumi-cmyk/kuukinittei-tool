def load_line_templates() -> list[dict]:
    if os.path.exists(_SAVE_FILE):
        try:
            with open(_SAVE_FILE, "r", encoding="utf-8") as f:
                data = json.load(f)
            if isinstance(data, list) and len(data) > 0:
                return data
        except Exception:
            pass
    return [dict(t) for t in DEFAULT_LINE_TEMPLATES]


def save_line_templates(templates: list[dict]) -> None:
    with open(_SAVE_FILE, "w", encoding="utf-8") as f:
        json.dump(templates, f, ensure_ascii=False, indent=2)


def reset_line_templates() -> None:
    if os.path.exists(_SAVE_FILE):
        os.remove(_SAVE_FILE)


